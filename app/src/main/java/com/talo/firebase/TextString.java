package com.talo.firebase;

public class TextString {

     private String str1;
     private String str2;
     public TextString(){}
     public TextString(String text_1, String text_2){
         this.str1 = text_1;
         this.str2 = text_2;
     }
     public String getStr1(){
         return str1;
     }
     public String getStr2(){
         return str2;
     }
}
